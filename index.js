// 2. СЕЛЕКТОРИ
const questionElement = document.querySelector("#question");
const btnElements = document.querySelectorAll(".answer"); // Взима четирите бутона
const resultDisplay = document.querySelector("#otgovor");
const levelDisplay = document.getElementById("level");
const fiftyBtn = document.getElementById("fifty");

// ЗВУЦИ
const music = document.getElementById("music");
const correctSound = document.getElementById("correct");
const wrongSound = document.getElementById("wrong");

// 3. ПРОМЕНЛИВИ ЗА СЪСТОЯНИЕ
let correctCount = 0;
let wrongCount = 0;
let currentIndex = 0;
let fiftyUses = 6;
let isWaiting = false;

// 4. ФУНКЦИЯ ЗА ЗАРЕЖДАНЕ НА ВЪПРОС
function loadQuestion(index) {
    if (!questions[index]) return; // Защита от грешен индекс

    let current = questions[index];

    // Показваме бутоните и ги нулираме
    btnElements.forEach(btn => {
        btn.style.visibility = "visible";
        btn.disabled = false;
    });

    // Попълваме текста на въпроса
    questionElement.innerText = current.question;

    // Попълваме текста в span-овете с клас .answer-text
    btnElements.forEach((btn, i) => {
        const textSpan = btn.querySelector(".answer-text");
        if (textSpan) {
            textSpan.innerText = current.answers[i];
        }
    });
    // Обновяваме нивото
    updateLevel(index);
}

function updateLevel(index) {
    if (!levelDisplay) return;
    if (index < 25) levelDisplay.innerText = "🌱 Лесно";
    else if (index < 50) levelDisplay.innerText = "⚡ Средно ниво!";
    else if (index < 75) levelDisplay.innerText = "🔥 Трудно ниво!";
    else levelDisplay.innerText = "💀 Експертно ниво!";
}

// 5. ПРОВЕРКА НА ОТГОВОР
function checkAnswer(selectedText) {
    if (isWaiting) return;
    isWaiting = true;

    let correctAnswer = questions[currentIndex].correct;

    if (selectedText === correctAnswer) {
        resultDisplay.innerText = "Правилно! ✅";
        correctCount++;
        if (correctSound) {
            correctSound.currentTime = 0;
            correctSound.play();
        }
    } else {
        resultDisplay.innerText = `Грешно! ❌ Верният отговор е: ${correctAnswer}`;
        wrongCount++;
        if (wrongSound) {
            wrongSound.currentTime = 0;
            wrongSound.play();
        }
    }

    resultDisplay.style.visibility = "visible";

    // ПАУЗА ПРЕДИ СЛЕДВАЩИЯ ВЪПРОС
    setTimeout(() => {
        currentIndex++;
        isWaiting = false;

        if (currentIndex < questions.length) {
            resultDisplay.style.visibility = "hidden";
            loadQuestion(currentIndex);
        } else {
            // КРАЙ НА ИГРАТА - Текстът остава тук!
            resultDisplay.style.visibility = "visible";
            resultDisplay.innerHTML = `Играта приключи! 🏆<br>Верни: ${correctCount} от ${questions.length} | Грешни: ${wrongCount}`;
            questionElement.innerText = "ФИНАЛ";
            btnElements.forEach(b => b.style.visibility = "hidden");
        }
    }, 2500);
}

// 6. 50/50 ЛОГИКА
if (fiftyBtn) {
    fiftyBtn.addEventListener("click", () => {
        if (fiftyUses <= 0 || isWaiting) return;

        fiftyUses--;
        fiftyBtn.innerText = `50/50 (${fiftyUses})`;

        let correctAnswer = questions[currentIndex].correct;
        let wrongButtons = Array.from(btnElements).filter(btn => 
            btn.querySelector(".answer-text").innerText !== correctAnswer
        );

        // Махаме 2 от 3-те грешни
        let randomWrongToKeep = wrongButtons[Math.floor(Math.random() * wrongButtons.length)];
        
        wrongButtons.forEach(btn => {
            if (btn !== randomWrongToKeep) {
                btn.style.visibility = "hidden";
            }
        });

        if (fiftyUses === 0) {
            fiftyBtn.style.opacity = "0.4";
            fiftyBtn.disabled = true;
        }
    });
}

// 7. СЪБИТИЯ ЗА КЛИК
btnElements.forEach(btn => {
    btn.addEventListener("click", function() {
        const text = this.querySelector(".answer-text").innerText;
        checkAnswer(text);
    });
});

// Първоначално стартиране
document.addEventListener("DOMContentLoaded", () => {
    loadQuestion(0);
});

